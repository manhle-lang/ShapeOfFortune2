struct SpinResult
{
	/**
	 * The base bet amount for the spin.
	 * Set as betSize * betLevel * baseBet from profile.
	 * Used as a base for betAmount, payout, and maxWinQuota calculations.
	 */
	double baseBetAmount;

	/**
	 * The total bet amount player bet for the spin.
	 * Equals baseBetAmount in MainGameSpin; set to 0 in FreeGameSpin (free spins).
	 * Used in payout and maxWinQuota calculations.
	 */
	double betAmount;

	/**
	 * The total payout amount for the spin.
	 * Calculated at the end of each spin. Capped by maxWinQuota.
	 */
	double payout;

	/**
	 * The payout amount of the spin, used solely for altering the multiplier result.
	 * Ignore this variable for front end display.
	 */
	double checkPayout;

	/**
	 * The maximum win quota for the spin. Used to cap payout.
	 * Set as baseBetAmount * maxWinMultiplier from profile in MainGameSpin.
	 * Updated in FreeGameSpin as remaining quota from pastSpinResult.
	 */
	double maxWinQuota;

	/**
	 * The main result vector representing symbols on the reels after the spin.
	 * Each symbol is reflected by each of their indexes.
	 */
	vector<int> resultVector;

	/**
	 * The vector to reflect the multiplier counters on top of the reels.
	 * Possible value : -1 to 9, -1 indicating blank, and the others indicate the number otherwise.
	 */
	vector<int> multiplierReelVector;

	/**
	 * Vector to display the winning positions of the reels.
	 */
	vector<bool> winningVector;

	/**
	 * Data containing the winning positions, payout and the information of the paylines.
	 */
	vector<CommonLibrary::Winline> winLines;

	/**
	 * The selection mode of the volatiflex reflecting the player selection. 0 - Circle | 1 - Triangle | 2 - Diamond
	 */
	int selectionMode;

	/**
	 * The selection mode of the volatiflex reflecting the player selection. 0 - Circle | 1 - Triangle | 2 - Diamond
	 */
	int reelMode;

	/**
	 * The multiplier value player awarded from the mutliplier reel feature. Applied to total winning of the spin if any.
	 */
	int multiplier;

	/**
	 * Free spin variable to indicate if the free game is triggered.
	 * Ignore this info for front end display for this game.
	 */
	int freeSpins;

	/**
	 * Counter to keep track of the total free spin the free game re-spinned.
	 * Ignore this info for front end display for this game.
	 */
	int totalFreeSpinsCount;

	/**
	 * Boolean to check if the max win will be triggered this round.
	 * Ignore this info for front end display for this game.
	 */
	bool triggerMaxWin;

	/**
	 * Boolean to indicate if there's teaser triggered for this round when there's no free game.
	 * Value = true to display the teaser animation for front end.
	 */
	bool teaser;

	/**
	 * Boolean to indicate if player selected feature buy option.
	 * Value = true to display the teaser animation for front end.
	 */
	bool featureBuy;

	/**
	 * Accumulated win amount for the current free game cycle.
	 * Updated in FreeGameSpin as payout is added. Linked to payout and maxWinQuota.
	 */
	double totalFreeGameWin;
};

struct SpinRequest
{
	double betSize;
	int betLevel;
	SpinResult pastSpinResult;
	SpinResult parentSpintResult;
};
