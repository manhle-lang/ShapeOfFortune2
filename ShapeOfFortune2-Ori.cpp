// Function to generate the reel strip based on the game mode
const vector<vector<int>> &GetReelStrip(const int &reelMode, const int &shapeMode, bool freeGame)
{
	// Return reel strips
	if (shapeMode == circleIndex)
	{
		if (freeGame)
			return fgReelStripCircle[reelMode];
		else
			return reelStripCircle[reelMode];
	}
	else if (shapeMode == triangleIndex)
	{
		if (freeGame)
			return fgReelStripTriangle[reelMode];
		else
			return reelStripTriangle[reelMode];
	}
	else if (shapeMode == diamondIndex)
	{
		if (freeGame)
			return fgReelStripDiamond[reelMode];
		else
			return reelStripDiamond[reelMode];
	}
}

// Function to generate the multiplier on the multiplier reels
int GenerateMultiplierReel(vector<int> &multiplierReelVector, const int &shapeMode, const int &reelMode, const double &checkPayout, const bool &freeGame)
{
	int multiplierSum = 0;

	bool changeMultiplier = false;

	// Alternate the multiplier if there's no payout
	if (checkPayout == 0.0 && library.RandomInt(1, 10 + 1) <= 2)
		changeMultiplier = true;

	for (int i = 0; i < multiplierReelVector.size(); i++)
	{
		// Check if this position is a blocker
		if (multiplierReelVector[i] != library.blockerIndex)
		{
			if (!freeGame)
				multiplierReelVector[i] = library.GetWeightedIndex(multiplierWeightage[shapeMode][reelMode]) - 1;
			else
				multiplierReelVector[i] = library.GetWeightedIndex(fgMultiplierWeightage[shapeMode][reelMode]) - 1;
			if (changeMultiplier)
				multiplierReelVector[i] = library.RandomInt(0, 9 + 1);
		}
	}

	if (!changeMultiplier)
	{
		// Determine the total number of multiplier available
		// Triangle Mode
		if (shapeMode == triangleIndex)
		{
			int blankReel;
			if (!freeGame)
				blankReel = 2 - library.GetWeightedIndex(triangleMultiplierWeightage[reelMode]);
			else
				blankReel = 2 - library.GetWeightedIndex(fgTriangleMultiplierWeightage[reelMode]);

			// Determine which reel to be blank
			if (blankReel > 0)
			{
				int randomizeIndex = library.RandomInt(1, 2 + 1);
				if (freeGame)
					randomizeIndex = 1;

				if (!freeGame)
					multiplierReelVector[randomizeIndex] = library.blankIndex;
				else
					multiplierReelVector[randomizeIndex] = 0;
			}
		}

		// Diamond Mode
		else if (shapeMode == diamondIndex)
		{
			int blankReel;
			if (!freeGame)
				blankReel = 3 - library.GetWeightedIndex(diamondMultiplierWeightage[reelMode]);
			else
				blankReel = 3 - library.GetWeightedIndex(fgDiamondMultiplierWeightage[reelMode]);

			// Determine which reel to be blank
			if (blankReel > 0)
			{
				vector<int> blankCol = {0, 1, 2};

				while (blankReel > 0)
				{
					int randomizeIndex = library.RandomInt(0, blankCol.size());
					if (freeGame)
						randomizeIndex = 0;

					if (!freeGame)
						multiplierReelVector[blankCol[randomizeIndex]] = library.blankIndex;
					else
						multiplierReelVector[blankCol[randomizeIndex]] = 0;

					blankReel--;
					blankCol.erase(blankCol.begin() + randomizeIndex);
				}
			}
		}
	}

	int divider = 1;

	// Compute the multiplier to be returned
	for (int i = multiplierReelVector.size() - 1; i >= 0; i--)
	{
		// If it's not a blocker or blank
		if (multiplierReelVector[i] != library.blockerIndex && multiplierReelVector[i] != library.blankIndex)
		{
			multiplierSum = multiplierSum + (multiplierReelVector[i] * divider);
			divider *= 10;
		}
	}

	// If there's no valid multiplier, then at the minimum return 1
	multiplierSum = (multiplierSum == 0) ? 1 : multiplierSum;

	return multiplierSum;
}

// Function to simulate a spin of the free game
SpinResult FreeGameSpin(const SpinRequest spinRequest)
{
	SpinResult spinResult;

	// Initialization of the other vector
	spinResult.payout = 0.0;
	spinResult.checkPayout = 0.0;
	spinResult.resultVector = vector<int>(library.GetVectorSum(reelWindowSize));
	spinResult.multiplierReelVector = vector<int>(reelWindowSize.size(), 0);
	spinResult.selectionMode = playerSelectionMode;
	spinResult.multiplier = 0;
	spinResult.triggerMaxWin = false;

	// New free game triggered
	if (spinRequest.pastSpinResult.totalFreeSpinsCount == 0)
	{
		spinResult.baseBetAmount = spinRequest.betSize * spinRequest.betLevel * baseBet;
		spinResult.betAmount = spinRequest.betSize * spinRequest.betLevel * baseBet;
		spinResult.maxWinQuota = spinResult.betAmount * (double)maxWinMultiplier; // Reset max win quota
		spinResult.totalFreeSpinsCount = 1;
		spinResult.featureBuy = playerFeatureBuy;

		// Adjust bet amount if player playing feature buy
		if (spinResult.featureBuy)
			spinResult.betAmount = spinRequest.betSize * spinRequest.betLevel * featureBuyCost[spinResult.selectionMode];
	}

	// Continuation from previous free game
	else
	{
		spinResult.baseBetAmount = 0.0;
		spinResult.betAmount = 0.0;
		spinResult.totalFreeSpinsCount = spinRequest.pastSpinResult.totalFreeSpinsCount + 1;
		spinResult.maxWinQuota = spinRequest.pastSpinResult.maxWinQuota;
		spinResult.featureBuy = spinRequest.pastSpinResult.featureBuy;
	}

	// Circle Mode
	if (spinResult.selectionMode == circleIndex)
	{
		spinResult.multiplierReelVector[0] = library.blockerIndex;
		spinResult.multiplierReelVector[1] = library.blockerIndex;
	}

	// Triangle Mode
	else if (spinResult.selectionMode == triangleIndex)
		spinResult.multiplierReelVector[0] = library.blockerIndex;

	// Get the reel strip mode of the game
	spinResult.reelMode = library.GetWeightedIndex(fgReelWeightage[spinResult.selectionMode]);
	if (spinResult.featureBuy)
		spinResult.reelMode = library.GetWeightedIndex(fbReelWeightage[spinResult.selectionMode]);

	// Generate reel strip on screen
	library.GenerateResult(GetReelStrip(spinResult.reelMode, spinResult.selectionMode, true), reelWindowSize, spinResult.resultVector);

	// Circle Mode
	if (spinResult.selectionMode == circleIndex)
	{
		spinResult.resultVector[library.GetIndexFromColRow(1, 1, reelWindowSize)] = library.wildIndex;
	}

	// Diamond Mode
	if (spinResult.selectionMode == diamondIndex)
	{
		spinResult.resultVector[library.GetIndexFromColRow(1, 0, reelWindowSize)] = library.blockerIndex;
		spinResult.resultVector[library.GetIndexFromColRow(1, 2, reelWindowSize)] = library.blockerIndex;
	}

	// First reel and third reel will synced
	for (int row = 0; row < reelWindowSize.size(); row++)
		spinResult.resultVector[library.GetIndexFromColRow(2, row, reelWindowSize)] = spinResult.resultVector[library.GetIndexFromColRow(0, row, reelWindowSize)];

	// If there's no payout then alternate the multiplier
	spinResult.checkPayout = library.GetPayLinePayout(selectedPayLine, payline, paytable, reelWindowSize, spinResult.resultVector);

	// For triangle and diamond the first 2 spins are always 0 payout, re-generate the result until no win is generated
	if (spinResult.selectionMode == triangleIndex || spinResult.selectionMode == diamondIndex)
	{
		while (spinResult.totalFreeSpinsCount < 2 && spinResult.checkPayout > 0.0)
		{
			// Get the reel strip mode of the game
			spinResult.reelMode = library.GetWeightedIndex(fgReelWeightage[spinResult.selectionMode]);
			if (spinResult.featureBuy)
				spinResult.reelMode = library.GetWeightedIndex(fbReelWeightage[spinResult.selectionMode]);

			// Generate reel strip on screen
			library.GenerateResult(GetReelStrip(spinResult.reelMode, spinResult.selectionMode, true), reelWindowSize, spinResult.resultVector);

			// Diamond Mode
			if (spinResult.selectionMode == diamondIndex)
			{
				spinResult.resultVector[library.GetIndexFromColRow(1, 0, reelWindowSize)] = library.blockerIndex;
				spinResult.resultVector[library.GetIndexFromColRow(1, 2, reelWindowSize)] = library.blockerIndex;
			}

			// First reel and third reel will synced
			for (int row = 0; row < reelWindowSize.size(); row++)
				spinResult.resultVector[library.GetIndexFromColRow(2, row, reelWindowSize)] = spinResult.resultVector[library.GetIndexFromColRow(0, row, reelWindowSize)];

			// If there's no payout then alternate the multiplier
			spinResult.checkPayout = library.GetPayLinePayout(selectedPayLine, payline, paytable, reelWindowSize, spinResult.resultVector);
		}
	}

	// If there's already 5 spin and still no win, re-generate the result until a win is generated
	while (spinResult.totalFreeSpinsCount == 5 && spinResult.checkPayout == 0.0)
	{
		// Get the reel strip mode of the game
		spinResult.reelMode = library.GetWeightedIndex(fgReelWeightage[spinResult.selectionMode]);
		if (spinResult.featureBuy)
			spinResult.reelMode = library.GetWeightedIndex(fbReelWeightage[spinResult.selectionMode]);

		// Generate reel strip on screen
		library.GenerateResult(GetReelStrip(spinResult.reelMode, spinResult.selectionMode, true), reelWindowSize, spinResult.resultVector);

		// Circle Mode
		if (spinResult.selectionMode == circleIndex)
			spinResult.resultVector[library.GetIndexFromColRow(1, 1, reelWindowSize)] = library.wildIndex;

		// Diamond Mode
		if (spinResult.selectionMode == diamondIndex)
		{
			spinResult.resultVector[library.GetIndexFromColRow(1, 0, reelWindowSize)] = library.blockerIndex;
			spinResult.resultVector[library.GetIndexFromColRow(1, 2, reelWindowSize)] = library.blockerIndex;
		}

		// First reel and third reel will synced
		for (int row = 0; row < reelWindowSize.size(); row++)
			spinResult.resultVector[library.GetIndexFromColRow(2, row, reelWindowSize)] = spinResult.resultVector[library.GetIndexFromColRow(0, row, reelWindowSize)];

		// If there's no payout then alternate the multiplier
		spinResult.checkPayout = library.GetPayLinePayout(selectedPayLine, payline, paytable, reelWindowSize, spinResult.resultVector);
	}

	// Geenerate multiplier on multiplier reel
	spinResult.multiplier = GenerateMultiplierReel(spinResult.multiplierReelVector, spinResult.selectionMode, spinResult.reelMode, spinResult.checkPayout, true);

	// Get the payout of the game
	spinResult.payout = min((double)library.GetPayLinePayout(selectedPayLine, payline, paytable, reelWindowSize, spinResult.resultVector) * spinResult.multiplier, spinResult.maxWinQuota);

	// If there's no payout then continue the free game
	if (spinResult.payout == 0.0)
		spinResult.freeSpins = 1;
	else
		spinResult.freeSpins = 0;

	// Get the front end data for the payline
	if (spinResult.payout > 0.0)
	{
		spinResult.winLines = library.GetPayLineData();
		spinResult.winningVector = library.GetWinningVector();

		if (spinResult.payout == spinResult.maxWinQuota)
			spinResult.triggerMaxWin = true;
	}

	return spinResult;
}

// Function to simulate a spin of the game
